					<h2>Un poco de <strong>historia</strong></h2>
					<p>
						La cooperativa de Luz y Fuerza de Cabildo Ltda., fue creada el 28 de julio de 1956 (no obstante la primera usina comenzó a funcionar en septiembre de 1926 donde hoy rige el cuartel de Bomberos voluntarios).
						El primer consejo de administración estuvo formado por Raúl Rayes, Rafael Bastan, Rubén Porte, Manuel García Ahargo, Rosendo Fernández, Amador Diez, Aldo Rossi, Nicolás Scaringi, Teofanes Villar, Pelegrino Rossi, Marcos Cohen, Domingo Ferrari, Pedro Monteserin ,Ramón Garaban y Antonio Belber.
					</p>
					<p>
						La interconexión con DEBA (hoy EDES SA) se realizó el 24 de agosto de 1963.
					</p>
					<p>
						En realidad, es la única cooperativa eléctrica en el partido de Bahía Blanca con la atención de 1.456 usuarios Urbanos en la localidad de Cabildo, 226 usuarios en el Country La Reserva, La Cañada y la Costa.
					</p>
					<p>
						Desde el 10 de noviembre de 1971 se brinda el servicio de Electrificación Rural  en la actualidad contamos con  316 usuarios en las zonas de Coronel Falcón, Estomba, Cochrane, Corti, La vitícola, Naposta y Cabildo.
					</p>
					<p>
						En 1979, la Cooperativa realizo por administración la construcción de la Central Automática de Teléfonos con que cuenta la localidad de Cabildo.
					</p>
					<p>
						En Noviembre de 1987, adquirió una ambulancia, un coche porta corona, un fúnebre y demás accesorios para la prestación de servicios sociales.
						Actualmente y ya en instalaciones propias de alrededor de 250 metros cuadrados, presta ese servicio con una adhesión de 868 adherentes.
						El servicio se cobra por grupo familiar con el recibo mensual de energía eléctrica.
					</p>
					<p>
						Desde junio de 2006, brinda el servicio de Telefonía celular corporativa a 612 adherentes al sistema.
					</p>
					<p>
						Desde el año 1990, la cooperativa se encuentra abocada en el sector rural al recambio de la empalizada de madera por Hormigón Armado, quedando a la fecha 30 postes a recambiar, ataduras , aisladores de porcelana por orgánicos, seccionadores y descargadores, además se les hizo a los transformadores  los  controles de PCB.			
					</p>
